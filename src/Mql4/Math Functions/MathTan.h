/**
 * @brief The function returns a tangent of a number.
 *
 * @param rad [in]  Angle in radians.
 * @return double Tangent of rad. If rad is greater than or equal to 263, or less than or equal to -263, a loss of significance in the result occurs, in which case the function returns an indefinite number.
 *
 * @note Instead of MathTan() you can use tan().
 */
double MathTan(
    double rad  // argument in radians
);

