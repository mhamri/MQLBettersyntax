/**
 * @brief The function raises a base to a specified power.
 * 
 * @param base [in]  Base.
 * @param exponent [in]  Exponent value.
 * @return double Value of base raised to the specified power.
 * 
 * @note Instead of MathPow() you can use pow().
 */
double MathPow(
    double base,    // base
    double exponent // exponent value
);

