/**
 * @brief Returns the logarithm of a number by base 10.
 *
 * @param val [in]  Numeric value the common logarithm of which is to be calculated.
 * @return double The common logarithm in case of success. If val is negative, the function returns NaN (undetermined value). If val is equal to 0, the function returns INF (infinity).
 *
 * @note Instead of MathLog10() you can use log10().
 */
double MathLog10(
    double val  // number to take logarithm
);

