/**
 * @brief The function returns the arc tangent of x. If x is equal to 0, the function returns 0.
 *
 * @param value [in]  A number representing a tangent.
 * @return double MathArctan returns a value within the range of -π/2 to π/2 radians.
 *
 * @note Instead of the MathArctan() function you can use atan().
 */
double MathArctan(
    double value  // tangent
);

/**
 * @brief The function returns the arc tangent of x. If x is equal to 0, the function returns 0.
 *
 * @param value [in]  A number representing a tangent.
 * @return double MathArctan returns a value within the range of -π/2 to π/2 radians.
 *
 * @note Instead of the MathArctan() function you can use atan().
 */
double atan(
    double value  // tangent
);