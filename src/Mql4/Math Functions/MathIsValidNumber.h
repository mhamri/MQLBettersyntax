/**
 * @brief It checks the correctness of a real number.
 *
 * @param number [in]  Checked numeric value.
 * @return true It returns true, if the checked value is an acceptable real number.
 * @return false If the checked value is a plus or minus infinity, or "not a number" (NaN), the function returns false.
 */
bool MathIsValidNumber(
    double number  // number to check
);