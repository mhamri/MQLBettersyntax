/**
 * @brief Returns the sine of a specified angle.
 *
 * @param value [in]  Angle in radians.
 * @return double Sine of an angle measured in radians. Returns value within the range of -1 to 1.
 *
 * @note Instead of MathSin() you can use sin().
 */
double MathSin(
    double value  // argument in radians
);

/**
 * @brief Returns the sine of a specified angle.
 *
 * @param value [in]  Angle in radians.
 * @return double Sine of an angle measured in radians. Returns value within the range of -1 to 1.
 *
 * @note Instead of MathSin() you can use sin().
 */
double sin(
    double value  // argument in radians
);