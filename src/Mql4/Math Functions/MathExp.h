/**
 * @brief The function returns the value of e raised to the power of d.
 *
 * @param value [in]  A number specifying the power.
 * @return double A number of double type. In case of overflow the function returns INF (infinity), in case of underflow MathExp returns 0.
 *
 * @note Instead of MathExp() you can use exp().
 */
double MathExp(
    double value  // power for the number e
);

/**
 * @brief The function returns the value of e raised to the power of d.
 *
 * @param value [in]  A number specifying the power.
 * @return double A number of double type. In case of overflow the function returns INF (infinity), in case of underflow MathExp returns 0.
 *
 * @note Instead of MathExp() you can use exp().
 */
double exp(
    double value  // power for the number e
);