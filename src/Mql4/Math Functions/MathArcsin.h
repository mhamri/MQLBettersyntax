/**
 * @brief The function returns the arc sine of x within the range of -π/2 to π/2 radians.
 *
 * @param val [in]   The val value between -1 and 1, the arc sine of which is to be calculated.
 * @return double Arc sine of the val number in radians within the range of -π/2 to π/2 radians. If val is less than -1 or more than 1, the function returns NaN (indeterminate value).
 *
 * @note Instead of the MathArcsin() function you can use asin().
 */
double MathArcsin(
    double val  // -1<value<1
);

/**
 * @brief The function returns the arc sine of x within the range of -π/2 to π/2 radians.
 *
 * @param val [in]   The val value between -1 and 1, the arc sine of which is to be calculated.
 * @return double Arc sine of the val number in radians within the range of -π/2 to π/2 radians. If val is less than -1 or more than 1, the function returns NaN (indeterminate value).
 *
 * @note Instead of the MathArcsin() function you can use asin().
 */
double asin(
    double val  // -1<value<1
);