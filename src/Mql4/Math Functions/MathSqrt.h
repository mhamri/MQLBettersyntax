/**
 * @brief Returns the square root of a number.
 *
 * @param value [in]  Positive numeric value.
 * @return double Square root of value. If value is negative, MathSqrt returns NaN (indeterminate value).
 *
 * @note Instead of MathSqrt() you can use sqrt().
 */
double MathSqrt(
    double value  // positive number
);

/**
 * @brief Returns the square root of a number.
 *
 * @param value [in]  Positive numeric value.
 * @return double Square root of value. If value is negative, MathSqrt returns NaN (indeterminate value).
 *
 * @note Instead of MathSqrt() you can use sqrt().
 */
double sqrt(
    double value  // positive number
);