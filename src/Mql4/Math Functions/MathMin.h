/**
 * @brief The function returns the minimal value of two values.
 *
 * @param value1 [in]  First numeric value.
 * @param value2 [in]  Second numeric value.
 * @return double The smallest of the two values.
 *
 * @note Instead of MathMin() you can use fmin().
 */
double MathMin(
    double value1,  // first value
    double value2   // second value
);

/**
 * @brief The function returns the minimal value of two values.
 *
 * @param value1 [in]  First numeric value.
 * @param value2 [in]  Second numeric value.
 * @return double The smallest of the two values.
 *
 * @note Instead of MathMin() you can use fmin().
 */
double fmin(
    double value1,  // first value
    double value2   // second value
);